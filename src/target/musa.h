#pragma once

#include <tvm/runtime/logging.h>

/**
 * Tensor descriptor data type
 */
typedef enum MUtensorDescriptorDataType_enum {
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_INT8 = 0,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_UINT8,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_INT16,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_UINT16,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT16,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_BFLOAT16,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_INT32,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_UINT32,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT32,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_TFLOAT32,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_INT64,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_UINT64,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT64,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT32_FTZ,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_TFLOAT32_FTZ,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT32_TFLOAT32_RNE,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_FLOAT32_FTZ_TFLOAT32_RNE,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_16b4,
  MU_TENSOR_DESCRIPTOR_DATA_TYPE_16b4_b8
} MUtensorDescriptorDataType;

/**
 * Tensor descriptor interleave layout type
 */
typedef enum MUtensorDescriptorInterleave_enum {
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_NONE = 0,
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_16B,
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_32B,
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_64B,
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_128B,
  MU_TENSOR_DESCRIPTOR_INTERLEAVE_256B
} MUtensorDescriptorInterleave;

typedef enum MUsmemSwizzleGranularity_enum {
  MU_SMEM_SWIZZLE_GRANULARITY_NONE = 0,
  MU_SMEM_SWIZZLE_GRANULARITY_B16,
  MU_SMEM_SWIZZLE_GRANULARITY_B32,
  MU_SMEM_SWIZZLE_GRANULARITY_B64,
} MUsmemSwizzleGranularity;

inline constexpr const char *ToString(MUsmemSwizzleGranularity sg) {
  switch (sg) {
  case MU_SMEM_SWIZZLE_GRANULARITY_NONE:
    return "SmemSwizzleGranularity::NONE";
  case MU_SMEM_SWIZZLE_GRANULARITY_B16:
    return "SmemSwizzleGranularity::B16";
  case MU_SMEM_SWIZZLE_GRANULARITY_B32:
    return "SmemSwizzleGranularity::B32";
  case MU_SMEM_SWIZZLE_GRANULARITY_B64:
    return "SmemSwizzleGranularity::B64";
  default:
    LOG(FATAL) << "Unknown Swizzle Granularity " << sg;
    return "";
  }
}
